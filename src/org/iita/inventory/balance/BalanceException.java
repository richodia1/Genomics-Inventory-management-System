package org.iita.inventory.balance;

/**
 * Exception thrown when reading weighing balance data.
 * 
 * @author mobreza
 * 
 */
public class BalanceException extends Exception {
	/**
	 * 
	 */
	private static final long serialVersionUID = -6218481831615099640L;

	public BalanceException() {
		super();
	}

	public BalanceException(String message) {
		super(message);
	}

	public BalanceException(String message, Exception innerException) {
		super(message, innerException);
	}
}
