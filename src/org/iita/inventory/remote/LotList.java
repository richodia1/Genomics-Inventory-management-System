package org.iita.inventory.remote;

import java.io.Serializable;
import java.util.List;

/**
 * Lot list by barcodes
 * 
 * @author mobreza
 */
public class LotList implements Serializable {
	private static final long serialVersionUID = 2585894514310236527L;
	private Long id;
	private String name;
	private List<Long> selectedLots;

	/**
	 * @return the id
	 */
	public Long getId() {
		return this.id;
	}

	/**
	 * @param id the id to set
	 */
	public void setId(Long id) {
		this.id = id;
	}

	/**
	 * @return the name
	 */
	public String getName() {
		return this.name;
	}

	/**
	 * @param name the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}

	/**
	 * @return the lotIds
	 */
	public List<Long> getSelectedLots() {
		return this.selectedLots;
	}
	
	/**
	 * @param lotIds the lotIds to set
	 */
	public void setSelectedLots(List<Long> lotIds) {
		this.selectedLots = lotIds;
	}
}
